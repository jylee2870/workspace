package sga.sol.ac.core.entity.policy;

public class PolicyParamEquip {
	String policyId;
	int equipRecid;
	int equipGroupRecid;
	int equipAccountRecid;
	
	public String getPolicyId() {
		return policyId;
	}
	public void setPolicyId(String policyId) {
		this.policyId = policyId;
	}
	public int getEquipRecid() {
		return equipRecid;
	}
	public void setEquipRecid(int equipRecid) {
		this.equipRecid = equipRecid;
	}
	public int getEquipGroupRecid() {
		return equipGroupRecid;
	}
	public void setEquipGroupRecid(int equipGroupRecid) {
		this.equipGroupRecid = equipGroupRecid;
	}
	public int getEquipAccountRecid() {
		return equipAccountRecid;
	}
	public void setEquipAccountRecid(int equipAccountRecid) {
		this.equipAccountRecid = equipAccountRecid;
	}

}	
