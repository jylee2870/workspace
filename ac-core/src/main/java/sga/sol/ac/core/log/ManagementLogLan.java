package sga.sol.ac.core.log;

public class ManagementLogLan {

	private String en;
	private String ja;
	private String ko;
	private String zh;
	
	public String getEn() {
		return en;
	}

	public String getJa() {
		return ja;
	}

	public String getKo() {
		return ko;
	}

	public String getZh() {
		return zh;
	}
	
	public ManagementLogLan(String ko, String zh, String en, String ja)
	{
		this.ko = ko;
		this.zh = zh;
		this.en = en;
		this.ja = ja;
	}
}
